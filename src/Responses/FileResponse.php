<?php
namespace Kaleu62\PIReader\Responses;

class FileResponse{

    /**
     * @var
     */
    public $content;

    /**
     * @var String From TypeConstants
     */
    public $type;

    /**
     * @return mixed
     */
    public function getContent()
    {
        return $this->content;
    }

    /**
     * @param mixed $content
     */
    public function setContent($content): void
    {
        $this->content = $content;
    }

    /**
     * @return mixed
     */
    public function getType()
    {
        return $this->type;
    }

    /**
     * @param mixed $type
     */
    public function setType($type): void
    {
        $this->type = $type;
    }

}